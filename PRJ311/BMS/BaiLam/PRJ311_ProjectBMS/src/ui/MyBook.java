    private List<DefaultDisplay> findByID(String id) {
        List<DefaultDisplay> newList = new Vector<>();
        for (DefaultDisplay object : list) {
            if (object.getTitleID().toLowerCase().contains(id)) {
                newList.add(object);
            }
        }
        return newList;
    }
