    private void loadJoinTable() {
        jTable1.setModel(tbModel = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
//        ListSelectionModel listMod = jTable1.getSelectionModel();
//        
        
        
        tbModel.setColumnIdentifiers(new String[]{"Book ID", "Book Title", "Publishers", "Authors", "Notes"});
        try {
            list = new DefaultDisplayDAO().selectDefault();
            for (DefaultDisplay book : list) {
                tbModel.addRow(book.toDataRow());
            }
        } catch (SQLException ex) {
        }
        jLabel4.setText("Record found: " + list.size());
    }
 
    private void setSearchTable(List<DefaultDisplay> vec) {
        tbModel.setRowCount(0);
        for (DefaultDisplay df : vec) {
            tbModel.addRow(df.toDataRow());
        }
        jLabel4.setText("Record found: " + vec.size());
    }

