    public Font getNewText() {
        String family = jlistFont.getSelectedValue();
        int style = 0;
        if (jListStyle.getSelectedIndex() == 1) {
            style = Font.BOLD;
        }
        if (jListStyle.getSelectedIndex() == 2) {
            style = Font.ITALIC;
        }
        if (jListStyle.getSelectedIndex() == 3) {
            style = Font.BOLD + Font.ITALIC;
        }
        if (jListStyle.getSelectedIndex() == 0) {
            style = Font.PLAIN;
        }
        String size = jListSize.getSelectedValue();
        return new Font(family, style, Integer.valueOf(size) );
    }
