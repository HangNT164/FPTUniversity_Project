    private final DefaultListModel<String> listFont;

    public MyFont(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        // load all system fonts to list
        jlistFont.setModel(listFont = new DefaultListModel<>());
        Font[] fonts = GraphicsEnvironment.getLocalGraphicsEnvironment().getAllFonts();
        for (Font f : fonts) {
            String family = f.getFamily();
            boolean found = false;
            for (int i = 0; i < listFont.size(); i++) {
                if (listFont.get(i).equals(family)) {
                    found = true;
                }
            }
            if (!found) {
                listFont.addElement(family);
            }
        }
    }

